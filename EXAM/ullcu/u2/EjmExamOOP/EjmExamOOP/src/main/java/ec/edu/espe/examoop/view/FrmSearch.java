    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
     String id = txtSearchId.getText();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, introduzca un ID de mascara.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        List<Document> results = cloudDB.searchCustomerById(id);
        DefaultTableModel model = (DefaultTableModel) tblResults.getModel();
        model.setRowCount(0); // Clear existing rows

        for (Document doc : results) {
            model.addRow(new Object[]{
                doc.getString("id"),
                doc.getString("material"),
                doc.getString("color"),
                doc.getString("size"),
                doc.getString("type")
            });
        }

        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No se encontró ningún cliente con el ID ingresado.", "Not Found", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

