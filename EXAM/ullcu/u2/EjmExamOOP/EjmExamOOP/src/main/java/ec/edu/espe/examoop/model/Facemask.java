package ec.edu.espe.examoop.model;

/**
 *
 * @author Alexander Ullco
 */
public class Facemask {
    private String id;
    private String material;
    private String color;
    private String size;
    private String type;

    public Facemask(String id, String material, String color, String size, String type) {
        this.id = id;
        this.material = material;
        this.color = color;
        this.size = size;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

}
