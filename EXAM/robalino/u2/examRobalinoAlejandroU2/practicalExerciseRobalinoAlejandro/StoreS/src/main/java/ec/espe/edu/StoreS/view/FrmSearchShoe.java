    private void bntConfirmShoeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntConfirmShoeActionPerformed

        String idStr = txtSeachID.getText();
        txtSeachID.setText("");

        try {
            int id = Integer.parseInt(idStr);
            Document shoe = ShoeController.findShoeById(id);

            if (shoe != null) {
                String name = shoe.getString("name");
                float price = shoe.getDouble("price").floatValue();
                Object[] dataRow = new Object[]{id, name, price};

                FrmFoundShoe foundShoe = new FrmFoundShoe();
                FrmFoundShoe.addRowToTblShoeInformation(dataRow);
                foundShoe.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Plato no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese correctamente los datos", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_bntConfirmShoeActionPerformed

