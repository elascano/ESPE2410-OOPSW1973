    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        if("male".equals(cmbSex.getSelectedItem().toString())) {
            sex = 'm';
        }else {
            sex = 'f';
        }
        gender = cmdGender.getSelectedItem().toString();
        maritalStatus = cmbMaritalStatus.getSelectedItem().toString();
        Date selectedDate = jCalendar2.getDate();  
        dateOfBirth = Calendar.getInstance();
        dateOfBirth.setTime(selectedDate);
        favoriteSports = new ArrayList<>();
        favoriteSports = (ArrayList<String>) lstFavoriteSports.getSelectedValuesList();
        comments = txaComments.getText();
        
        Contact contact = new Contact(id, name, sex, gender, maritalStatus, dateOfBirth, favoriteSports, comments);
        
        Document contactDocument = new Document("id", contact.getId())
            .append("name", contact.getName())
            .append("sex", String.valueOf(contact.getSex()))
            .append("gender", contact.getGender())
            .append("maritalStatus", contact.getMaritalStatus())
            .append("dateOfBirth", contact.getDateOfBirth().getTime())  
            .append("favoriteSports", contact.getFavoriteSports())
            .append("comments", contact.getComments());

        
        boolean success = MongoDbManager.insert("contacts", contactDocument); 

        if (success) {
            JOptionPane.showMessageDialog(rootPane, "Successfully saved " + contact);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error saving " + contact);
        }

        
        System.out.println("Contact -->" + contact);
        
                
    }//GEN-LAST:event_btnAddActionPerformed

