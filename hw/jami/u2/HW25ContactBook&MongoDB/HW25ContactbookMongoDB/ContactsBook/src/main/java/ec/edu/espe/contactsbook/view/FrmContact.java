    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        if (cmbxSex.getSelectedItem().toString().equals("male")) {
            sex = 'm';
        } else {
            sex = 'f';
        }
        gender = cmbxGender.getSelectedItem().toString();
        maritalStatus = cmbxMaritalStatus.getSelectedItem().toString();
        dateOfBirth = calendar.getCalendar();
        favoriteSports = new ArrayList<>();
        favoriteSports = (ArrayList<String>) listFavoriteSports.getSelectedValuesList();
        comments = txtComments.getText();

        Contact contact = new Contact(id, name, sex, gender, maritalStatus, dateOfBirth, favoriteSports, comments);

        if (contactController.add(contact)) {
            JOptionPane.showMessageDialog(rootPane, "success" + contact);
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        Contact contact = (Contact) contactController.find(txtId.getText());

        if (contact == null) {
            txtId.setText("");
            txtName.setText("");
            cmbxSex.setSelectedItem("m");
            cmbxGender.setSelectedItem("man");
            cmbxMaritalStatus.setSelectedItem("married");
            favoriteSports.clear();
            txtComments.setText("");

        }

        txtId.setText(String.valueOf(contact.getId()));
        txtName.setText(contact.getName());
        btnDelete.setEnabled(true);


    }//GEN-LAST:event_btnFindActionPerformed

