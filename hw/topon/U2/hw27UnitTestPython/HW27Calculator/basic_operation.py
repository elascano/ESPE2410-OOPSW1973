class BasicOperation:
    def __init__(self, term1, term2):
        self.term1 = term1
        self.term2 = term2

    def get_term1(self):
        return self.term1

    def set_term1(self, term1):
        self.term1 = term1

    def get_term2(self):
        return self.term2

    def set_term2(self, term2):
        self.term2 = term2
