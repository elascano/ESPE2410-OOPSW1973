    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
   try {
       
        int id = Integer.parseInt(txtId.getText());
        String name = txtName.getText();

        char sex = "male".equals(cmbSex.getSelectedItem().toString()) ? 'm' : 'f';

        // Otros datos
        String gender = cmbGender.getSelectedItem().toString();
        String maritalStatus = cmbMaritalStatus.getSelectedItem().toString();

 
        Calendar dateOfBirth = Calendar.getInstance();
        dateOfBirth.set(Calendar.YEAR, 2024);
        dateOfBirth.set(Calendar.MONTH, 3); 
        dateOfBirth.set(Calendar.DAY_OF_MONTH, 17);
        
        ArrayList<String> favoriteSport = new ArrayList<>(lstFavoriteSport.getSelectedValuesList());
        
        String comments = txaComments.getText();

        Contact contact = new Contact(id, name, sex, gender, maritalStatus, dateOfBirth, favoriteSport, comments);

        if (contactController.add(contact)) {
            JOptionPane.showMessageDialog(rootPane, "Success saving " + contact);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error saving the contact.");
        }
        
        System.out.println("contact --->" + contact);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Error processing the data: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        // TODO add your handling code here:
        Contact contact = (Contact) contactController.find(txtId.getText());
        if (contact == null) {
            emptyFields();

        }
        txtId.setText(String.valueOf(contact.getId()));
        txtName.setText(contact.getName());
        cmbGender.setSelectedItem(contact.getGender());
        btnDelete.setEnabled(true);

    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (JOptionPane.showConfirmDialog(rootPane, "Are you sure") == JOptionPane.YES_OPTION) {
            System.out.println("Yes delete it");
            emptyFields();
            btnDelete.setEnabled(false);
        }
        if (JOptionPane.showConfirmDialog(rootPane, "Are you sure") == JOptionPane.NO_OPTION) {
            System.out.println("");
            emptyFields();
        }
        if (JOptionPane.showConfirmDialog(rootPane, "Are you sure") == 2) {
            System.out.println("");
            emptyFields();
        }


    }//GEN-LAST:event_btnDeleteActionPerformed

    private void emptyFields() {
        txtId.setText("");
        txtName.setText("");
        cmbSex.setSelectedItem('m');
        cmbGender.setSelectedItem("man");
        cmbMaritalStatus.setSelectedItem("married");
        favoriteSport.clear();
        txaComments.setText("");
    }

