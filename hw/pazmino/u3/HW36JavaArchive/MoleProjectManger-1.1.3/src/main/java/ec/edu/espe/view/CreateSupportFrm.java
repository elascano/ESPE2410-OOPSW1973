    private void btnCreateSupportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateSupportActionPerformed
        String detail= txaDetails.getText().trim();
        int durationInYears= Integer.parseInt(cmbDuration.getSelectedItem().toString());
        String scheduleType= getSelectedSchedule();
        
        if (scheduleType == null || scheduleType.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un tipo de horario.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Date startDate= new Date();
        Date endDate= supportController.calculateEndDateOfSupport(startDate, durationInYears);
        String supportId= supportController.generateSupportId(durationInYears, scheduleType);
        String supportStatus= "Creado";
        String projectId= project.getProjectId();
        String projectTitle= project.getProjectTitle();
        
        if (detail.isEmpty()) {
            JOptionPane.showMessageDialog(null, "El campo de detalles no puede estar vacío.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Support support = new Support(supportId, projectId, projectTitle, detail, startDate, endDate, supportStatus, durationInYears, scheduleType);
        
        supportController.saveSupport(support);
        JOptionPane.showMessageDialog(null, "Soporte creado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        
        ProjectForSupportFrm projectForSupportFrm= new ProjectForSupportFrm();
        projectForSupportFrm.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_btnCreateSupportActionPerformed

