    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id;
        String name;
        String address;
        String phoneNumber;
        String manager;
        boolean isValid;
        Branch branch;

        id = txtId.getText();
        branch = this.branchController.getBranch(id);

        if (branch != null) {
          

            name = txtName.getText();
            address = txtAddress.getText();
            phoneNumber = txtPhoneNumber.getText();
            manager = txtManager.getText();
           

                isValid = branchController.update(id, name, address, phoneNumber, manager);

                if (isValid) {
                    JOptionPane.showMessageDialog(rootPane, "Actualizado exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar", "Error", JOptionPane.ERROR_MESSAGE);
                }
           

        } else{
            JOptionPane.showMessageDialog(rootPane, "Id inválido", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed
    
    private void updateFields(String id) {
        if (id == null || id.trim().isEmpty()) {
            txtName.setText("");
            txtAddress.setText("");
            txtPhoneNumber.setText("");
            txtManager.setText("");
            
            txtName.setEnabled(false);
            txtAddress.setEnabled(false);
            txtPhoneNumber.setEnabled(false);
            txtManager.setEnabled(false);
            btnUpdate.setEnabled(false);
        } else {
            Branch branch = branchController.getBranch(id);
            
            if (branch != null) {
                
                String name = branch.getName();
                String address = branch.getAddress();
                String phoneNumber = branch.getPhoneNumber();
                String manager = branch.getManager();
                
                txtName.setText(name);
                txtAddress.setText(address);
                
                txtPhoneNumber.setText(phoneNumber);
                txtManager.setText(manager);
                
                txtName.setEnabled(true);
                txtAddress.setEnabled(true);
                txtPhoneNumber.setEnabled(true);
                txtManager.setEnabled(true);
                btnUpdate.setEnabled(true);
                
            } else {
                
                txtName.setText("");
                txtAddress.setText("");
                
                txtPhoneNumber.setText("");
                txtManager.setText("");
                
                txtName.setEnabled(false);
                txtAddress.setEnabled(false);
                
                txtPhoneNumber.setEnabled(false);
                txtManager.setEnabled(false);
                btnUpdate.setEnabled(false);
            }
        }
    }

