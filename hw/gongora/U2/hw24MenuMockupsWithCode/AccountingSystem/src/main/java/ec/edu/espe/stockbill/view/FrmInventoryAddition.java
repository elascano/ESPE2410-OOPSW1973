    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id;
        String reference;
        String description;
        float retailPrice;
        float wholesalePrice;
        float distributorPrice;
        int amount;
        String descriptionMeasuredItem;
        float valueMeasuredItem;
        String unitMeasuredItem;
        boolean isValid;
        
        id = txtID.getText();
        reference = txtReference.getText();
        description = txtDescription.getText();
        retailPrice = Float.parseFloat(txtRetailPrice.getText());
        wholesalePrice = Float.parseFloat(txtWholesalePrice.getText());
        distributorPrice = Float.parseFloat(txtDistributorPrice.getText());
        amount = Integer.parseInt(txtAmount.getText());
        descriptionMeasuredItem = txtMeasuredItemDescription.getText();
        valueMeasuredItem =Float.parseFloat(txtMeasuredItemValue.getText()) ;
        unitMeasuredItem = cmbxMeasuredItemUnit.getSelectedItem().toString();
        
        isValid = productController.add(id, reference, description, retailPrice, wholesalePrice, distributorPrice, amount, descriptionMeasuredItem, valueMeasuredItem, unitMeasuredItem);
        
        if (isValid) {
                JOptionPane.showMessageDialog(rootPane, "Añadido exitosamente", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo añadir", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

