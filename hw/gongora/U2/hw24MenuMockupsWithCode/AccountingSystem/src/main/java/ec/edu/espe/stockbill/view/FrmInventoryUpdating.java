    private void updateFields(String id) {
        if (id == null || id.trim().isEmpty()) {
            txtReference.setText("");
            txtDescription.setText("");
            txtRetailPrice.setText("");
            txtWholesalePrice.setText("");
            txtDistributorPrice.setText("");
            txtAmount.setText("");
            txtMeasuredItemDescription.setText("");
            txtMeasuredItemValue.setText("");
            cmbxMeasuredItemUnit.setSelectedItem("");

            txtReference.setEnabled(false);
            txtDescription.setEnabled(false);
            txtRetailPrice.setEnabled(false);
            txtWholesalePrice.setEnabled(false);
            txtDistributorPrice.setEnabled(false);
            txtAmount.setEnabled(false);
            txtMeasuredItemDescription.setEnabled(false);
            txtMeasuredItemValue.setEnabled(false);
            cmbxMeasuredItemUnit.setEnabled(false);
            btnUpdate.setEnabled(false);

        } else {
            Product product = productController.getPtroduct(id);

            if (product != null) {

                String reference = product.getReference();
                String description = product.getDescription();
                String retailPrice = String.valueOf(product.getPrice().getRetail());
                String wholesalePrice = String.valueOf(product.getPrice().getWholesale());
                String distributorPrice = String.valueOf(product.getPrice().getDistributor());
                String amount = String.valueOf(product.getAmount());
                String measuredItemDescription = product.getMeasuredItem().getDescription();
                String measuredItemValue = String.valueOf(product.getMeasuredItem().getValue());
                String measuredItemUnit = product.getMeasuredItem().getUnit();

                txtReference.setText(reference);
                txtDescription.setText(description);
                txtRetailPrice.setText(retailPrice);
                txtWholesalePrice.setText(wholesalePrice);
                txtDistributorPrice.setText(distributorPrice);
                txtAmount.setText(amount);
                txtMeasuredItemDescription.setText(measuredItemDescription);
                txtMeasuredItemValue.setText(measuredItemValue);
                cmbxMeasuredItemUnit.setSelectedItem(measuredItemUnit);

                txtReference.setEnabled(true);
                txtDescription.setEnabled(true);
                txtRetailPrice.setEnabled(true);
                txtWholesalePrice.setEnabled(true);
                txtDistributorPrice.setEnabled(true);
                txtAmount.setEnabled(true);
                txtMeasuredItemDescription.setEnabled(true);
                txtMeasuredItemValue.setEnabled(true);
                cmbxMeasuredItemUnit.setEnabled(true);
                btnUpdate.setEnabled(true);

            } else {
                txtReference.setText("");
                txtDescription.setText("");
                txtRetailPrice.setText("");
                txtWholesalePrice.setText("");
                txtDistributorPrice.setText("");
                txtAmount.setText("");
                txtMeasuredItemDescription.setText("");
                txtMeasuredItemValue.setText("");
                cmbxMeasuredItemUnit.setSelectedItem("");

                txtReference.setEnabled(false);
                txtDescription.setEnabled(false);
                txtRetailPrice.setEnabled(false);
                txtWholesalePrice.setEnabled(false);
                txtDistributorPrice.setEnabled(false);
                txtAmount.setEnabled(false);
                txtMeasuredItemDescription.setEnabled(false);
                txtMeasuredItemValue.setEnabled(false);
                cmbxMeasuredItemUnit.setEnabled(false);
                btnUpdate.setEnabled(false);
            }
        }
    }

