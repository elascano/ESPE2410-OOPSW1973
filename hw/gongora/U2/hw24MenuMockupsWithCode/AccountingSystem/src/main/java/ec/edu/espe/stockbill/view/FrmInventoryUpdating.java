    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String id;
        String reference;
        String description;
        float retailPrice;
        float wholesalePrice;
        float distributorPrice;
        int amount;
        String descriptionMeasuredItem;
        float valueMeasuredItem;
        String unitMeasuredItem;
        Product product;
        boolean isValid;

        id = txtID.getText();
        product = this.productController.getPtroduct(id);
        if (product != null) {
            reference = txtReference.getText();
            description = txtDescription.getText();
            retailPrice = Float.parseFloat(txtRetailPrice.getText());
            wholesalePrice = Float.parseFloat(txtWholesalePrice.getText());
            distributorPrice = Float.parseFloat(txtDistributorPrice.getText());
            amount = Integer.parseInt(txtAmount.getText());
            descriptionMeasuredItem = txtMeasuredItemDescription.getText();
            valueMeasuredItem = Float.parseFloat(txtMeasuredItemValue.getText());
            unitMeasuredItem = cmbxMeasuredItemUnit.getSelectedItem().toString();

            isValid = productController.update(id, reference, description, retailPrice, wholesalePrice, distributorPrice, amount, descriptionMeasuredItem, valueMeasuredItem, unitMeasuredItem);

            if (isValid) {
                JOptionPane.showMessageDialog(rootPane, "A√±adido exitosamente", "√âxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

