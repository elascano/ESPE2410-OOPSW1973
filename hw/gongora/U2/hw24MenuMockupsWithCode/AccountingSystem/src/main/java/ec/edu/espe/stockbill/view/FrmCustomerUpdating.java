    public void validateInputAccessAndCleanup(Customer customer) {
        boolean customerExist = customer != null;
        String type = "";
        if (customerExist) {
            switch (customer.getType()) {
                case "retail":
                    type = "minorista";
                    break;
                case "wholesale":
                    type = "mayorista";
                    break;
                case "distributor":
                    type = "distribuidor";
                    break;

            }
        }

        cmbxType.setSelectedItem((customerExist) ? type : "");
        txtName.setText((customerExist) ? customer.getName() : "");
        cmbxCredentialCardType.setSelectedItem((customerExist) ? customer.getIdentityCard().getType() : "");
        txtCredentialCardNumber.setText((customerExist) ? customer.getIdentityCard().getId() : "");
        txtAddress.setText((customerExist) ? customer.getAddress() : "");
        txtPhoneNumber.setText((customerExist) ? customer.getPhoneNumber() : "");
        txtEmail.setText((customerExist) ? customer.getEmail() : "");

        cmbxType.setEnabled(customerExist);
        txtName.setEnabled(customerExist);
        cmbxCredentialCardType.setEnabled(customerExist);
        txtCredentialCardNumber.setEnabled(customerExist);
        txtAddress.setEnabled(customerExist);
        txtPhoneNumber.setEnabled(customerExist);
        txtEmail.setEnabled(customerExist);
        btnUpdate.setEnabled(customerExist);
    }

