    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        String typeInSpanish;
        String type = "";
        String name;
        String identityCardType;
        String identityCardNumber;
        String address;
        String phoneNumber;
        String email;
        String id = txtID.getText().trim();
        Customer customer = customerController.getCustomer(id);
        boolean isValid = false;
        if (customer != null) {
            try {
                
                typeInSpanish = cmbxType.getSelectedItem().toString();
                switch (typeInSpanish) {
                    case "minorista":
                        type = "retail";
                        break;
                    case "mayorista":
                        type = "wholesale";
                        break;
                    case "distribuidor":
                        type = "distributor";
                        break;
                }
                name = txtName.getText();
                identityCardType = cmbxCredentialCardType.getSelectedItem().toString();
                identityCardNumber = txtCredentialCardNumber.getText();
                address = txtAddress.getText();
                phoneNumber = txtPhoneNumber.getText();
                email = txtEmail.getText();
                isValid = customerController.update(id, type, name, identityCardType, identityCardNumber, address, phoneNumber, email);
                if (isValid) {
                    JOptionPane.showMessageDialog(rootPane, "Se actualizó correctamente", "Confirmación", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (InvalidIdentityCardException ex) {
                JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

