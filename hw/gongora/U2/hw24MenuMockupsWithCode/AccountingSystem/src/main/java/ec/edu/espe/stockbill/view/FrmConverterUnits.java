    private void btnCalculateConversionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateConversionActionPerformed
        String sourceUnit = cmbxSourceUnit.getSelectedItem().toString();
        String finalUnit = cmbxDestinyUnit.getSelectedItem().toString();
        float valueOfFinalUnit;
        try {
            Float value = Float.parseFloat(txtValueOfSourceUnit.getText());
            valueOfFinalUnit=conversionController.conversion(value, sourceUnit, finalUnit);
            lblValueOfDestinyUnit.setText(String.valueOf(valueOfFinalUnit));

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(rootPane, "El valor  de la unidad de origen debe ser un n√∫mero","Error",JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnCalculateConversionActionPerformed

