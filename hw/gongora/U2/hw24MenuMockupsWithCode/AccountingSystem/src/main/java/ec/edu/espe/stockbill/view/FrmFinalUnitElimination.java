    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String sourceUnit;
        String finalUnit;
        boolean isConversionDeleted;
        
        sourceUnit = cmbxSourceUnit.getSelectedItem().toString();
        finalUnit = cmbxDestinyUnit.getSelectedItem().toString();
    
        isConversionDeleted = conversionController.deleteSubUnit(sourceUnit, finalUnit);
        if (isConversionDeleted){
            JOptionPane.showMessageDialog(rootPane, "La conversion se eliminó  correctamente", "Éxito",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "La conversion no se ha podido eliminar", "Error",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

