    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
        float initialCapital = Float.parseFloat(jTextField1.getText());
        int interestRate = Integer.parseInt(jTextField2.getText());
        int time = Integer.parseInt(jTextField4.getText());

        BankInterestRate bankInterestController = new BankInterestRate(initialCapital, interestRate, time);

        BankInterestRateController bankInterestRateController = new BankInterestRateController(bankInterestController);

        double interest = bankInterestRateController.calculateInterest();

        jTextField5.setText(String.valueOf(interest));

        MongoDBManager mongoDBManager = new MongoDBManager();

        mongoDBManager.connect();

        JOptionPane.showMessageDialog(this, "Data saved to MongoDB!");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter valid numeric values.");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error while saving data to MongoDB: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed

