    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            double incomes = 0;
            double expenses = 0;

            if (!txtIncomes.getText().trim().isEmpty()) {
                incomes = Double.parseDouble(txtIncomes.getText());
            }

            if (!txtExpenses.getText().trim().isEmpty()) {
                expenses = Double.parseDouble(txtExpenses.getText());
            }

            if (incomes > 0) {
                Transaction incomeTransaction = new Transaction(incomes);
                controller.addTransaction(incomeTransaction);
            }
            if (expenses > 0) {
                Transaction expenseTransaction = new Transaction(expenses, true); 
                controller.addTransaction(expenseTransaction);
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Transaction saved successfully!");
            
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Invalid input! Please enter valid numbers.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

