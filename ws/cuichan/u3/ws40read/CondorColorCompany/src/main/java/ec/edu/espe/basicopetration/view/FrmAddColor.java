    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        try {
            String colorName = txtColorName.getText();
            String colorId = txtID.getText();
            String type = rbtnBalde.isSelected() ? "Lata" : "Spray";
            double price = Double.parseDouble(txtPrice.getText());

            if (colorName.isEmpty() || colorId.isEmpty() || txtPrice.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,
                        "Todos los campos deben estar completos",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Color color = new Color(colorName, colorId, type, price);

            MongoDBManager.getInstance().addColor(color);

            JOptionPane.showMessageDialog(this,
                    "Color agregado con éxito",
                    "Éxito", JOptionPane.INFORMATION_MESSAGE);

            txtColorName.setText("");
            txtID.setText("");
            txtPrice.setText("");
            rbtnBalde.setSelected(true);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "El precio debe ser un número válido",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this,
                    "Error al guardar: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAcceptActionPerformed

