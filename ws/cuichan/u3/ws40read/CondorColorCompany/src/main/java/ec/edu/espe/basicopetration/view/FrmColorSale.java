    public void addColorToSale(String colorName, String colorId, String type, double price) {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tSale.getModel();
// Verificar si la tabla ya tiene datos (filas en blanco)
        if (model.getRowCount() > 0 && model.getValueAt(0, 0) == null) {
            // Si la primera fila está vacía, usar esa fila en lugar de agregar una nueva
            model.setRowCount(0);
        }

        model.addRow(new Object[]{colorName,colorId, type, 1, price}); // 1 unidad por defecto
        updateTotal();

    }

// Método para actualizar el total cuando cambia la cantidad de unidades
    private void updateTotal() {
        double total = 0;
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tSale.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 3) != null && model.getValueAt(i, 4) != null) {
                try {
                    int units = Integer.parseInt(model.getValueAt(i, 3).toString());
                    double price = Double.parseDouble(model.getValueAt(i, 4).toString());
                    total += units * price;
                } catch (NumberFormatException e) {
                    // Ignorar filas con valores no numéricos
                }
            }
        }
        txtTotal.setText(String.format("%.2f", total));
    }

