    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
 
    try {
            // Get the amount and time from the text fields
            int id = Integer.parseInt(idtxt.getText());
            double amount = Double.parseDouble(amounttxt.getText());  // Amount input by user
            int time = Integer.parseInt(timetxt.getText());            // Time input by user

            // Call the controller to save the investment
            controller.saveInvestment(amount, time);

            // Inform the user that the data was saved successfully
            JOptionPane.showMessageDialog(this, "Investment saved successfully!");

            // Optionally clear the input fields after saving
            amounttxt.setText("");
            timetxt.setText("");

        } catch (NumberFormatException ex) {
            // Handle invalid input (non-numeric)
            JOptionPane.showMessageDialog(this, "Please enter valid numeric values.");
        }
    
        
    }//GEN-LAST:event_btnSaveActionPerformed

