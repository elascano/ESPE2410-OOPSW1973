    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        id = Integer.parseInt(txtId.getText());
        name = txtName.getText();
        if (cmbxSex.getSelectedItem().toString().equals("male")) {
            sex = 'm';
        } else {
            sex = 'f';
        }
        gender = cmbxGender.getSelectedItem().toString();
        maritalStatus = cmbxMaritalStatus.getSelectedItem().toString();


if (calendar.getDate() == null) {
        JOptionPane.showMessageDialog(rootPane, "Por favor selecciona una fecha válida.");
        return;
    }

    Date selectedDate = calendar.getDate();
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");

    try {
        String dateOfBirth = dateFormat.format(selectedDate);
        favoriteSports = new ArrayList<>();
        favoriteSports = (ArrayList<String>) listFavoriteSports.getSelectedValuesList();
        comments = txtComments.getText();

        Contact contact = new Contact(id, name, sex, gender, maritalStatus, dateOfBirth, favoriteSports, comments);

        // Guardar en MongoDB
        MongoCollection<Document> collection = MongoDBConnection.getCollection();
        Document document = new Document("id", contact.getId())
                .append("name", contact.getName())
                .append("sex", contact.getSex())
                .append("gender", contact.getGender())
                .append("maritalStatus", contact.getMaritalStatus())
                .append("dateOfBirth", contact.getDateOfBirth())
                .append("favoriteSports", contact.getFavoriteSports())
                .append("comments", contact.getComments());

        collection.insertOne(document);
        JOptionPane.showMessageDialog(rootPane, "success" + contact);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(rootPane, "Error al formatear la fecha.");
    }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        Contact contact = (Contact) contactController.find(txtId.getText());

        if (contact == null) {
            txtId.setText("");
            txtName.setText("");
            cmbxSex.setSelectedItem("m");
            cmbxGender.setSelectedItem("man");
            cmbxMaritalStatus.setSelectedItem("married");
            favoriteSports.clear();
            txtComments.setText("");

        }

        txtId.setText(String.valueOf(contact.getId()));
        txtName.setText(contact.getName());
        btnDelete.setEnabled(true);


    }//GEN-LAST:event_btnFindActionPerformed

