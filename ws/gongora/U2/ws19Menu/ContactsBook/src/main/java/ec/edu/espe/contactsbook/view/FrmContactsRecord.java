    private Object[] convertContactToRow(Contact contact) {
        return new Object[]{
            contact.getId(),
            contact.getName(),
            contact.getSex(),
            contact.getGender(),
            contact.getMaritalStatus(),
            contact.getDateOfBirth(),
            contact.getFavoriteSports(),
            contact.getComments()
        };
    }

