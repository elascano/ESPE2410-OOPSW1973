    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        try {
          // Limpiar tabla antes de cargar nuevos datos
          tableModel.setRowCount(0);

          // Cargar todos los contactos usando el nuevo método
          ArrayList<Contact> contacts = contactController.getAllContacts();

          for (Contact contact : contacts) {
              Calendar dob = contact.getDateOfBirth();
              String dateStr = String.format("%d-%02d-%02d", 
                  dob.get(Calendar.YEAR), 
                  dob.get(Calendar.MONTH) + 1, 
                  dob.get(Calendar.DAY_OF_MONTH));

              String sports = String.join(", ", contact.getFavoriteSports());

              Object[] row = {
                  contact.getId(),
                  contact.getName(),
                  contact.getSex(),
                  contact.getGender(),
                  contact.getMaritalStatus(),
                  dateStr,
                  sports,
                  contact.getComments()
              };

              tableModel.addRow(row);
          }

          // Mostrar mensaje de éxito con el número de contactos cargados
          if (contacts.size() > 0) {
              javax.swing.JOptionPane.showMessageDialog(
                  this,
                  "Successfully loaded " + contacts.size() + " contacts",
                  "Load Success",
                  javax.swing.JOptionPane.INFORMATION_MESSAGE
              );
          } else {
              javax.swing.JOptionPane.showMessageDialog(
                  this,
                  "No contacts found in the database",
                  "Information",
                  javax.swing.JOptionPane.INFORMATION_MESSAGE
              );
          }

      } catch (Exception e) {
          javax.swing.JOptionPane.showMessageDialog(
              this,
              "Error loading contacts: " + e.getMessage(),
              "Load Error",
              javax.swing.JOptionPane.ERROR_MESSAGE
          );
      }
    }//GEN-LAST:event_btnLoadActionPerformed

